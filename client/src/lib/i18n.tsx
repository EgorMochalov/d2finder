interface I18nCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, params?: Record<string, string>) => string;
}

const Ctx = createContext<I18nCtx>({
  lang: 'ru',
  setLang: () => {},
  t: (k: string) => k,
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => (localStorage.getItem('lang') as Lang) || 'ru');
  const t: I18nCtx['t'] = (key, params) => {
    let s = translations[lang][key] || translations.ru[key] || key;
    if (params) Object.entries(params).forEach(([k, v]) => { s = s.replace(`{${k}}`, v); });
    return s;
  };
  return (
    <Ctx.Provider value={{ lang, setLang: (l) => { setLang(l); localStorage.setItem('lang', l); }, t }}>
      {children}
    </Ctx.Provider>
  );
}

export function useI18n() { return useContext(Ctx); }

const ERROR_MAP: Record<string, string> = {
  'Invalid credentials': 'errors.invalid_credentials',
  'User already exists': 'errors.user_exists',
  'Invalid email or password': 'errors.invalid_credentials',
  'Invalid username or password': 'errors.invalid_credentials',
};

export function translateApiError(err: unknown, t: (key: string) => string): string {
  const msg = err && typeof err === 'object' && 'message' in err ? (err as any).message : String(err);
  return t(ERROR_MAP[msg] || 'errors.server_error');
}
